public class Carro {
    private int anoDoCarro;
    private String nomeModelo;
    private float precoPorAno;

    public int getAnoDoCarro() {
        return anoDoCarro;
    }

    public void setAnoDoCarro(int anoDoCarro) {
        this.anoDoCarro = anoDoCarro;
    }

    public String getNomeModelo() {
        return nomeModelo;
    }

    public void setNomeModelo(String nomeModelo) {
        this.nomeModelo = nomeModelo;
    }

    public float getPrecoPorAno() {
        return precoPorAno;
    }

    public void setPrecoPorAno(float precoPorAno) {
        this.precoPorAno = precoPorAno;
    }

}
